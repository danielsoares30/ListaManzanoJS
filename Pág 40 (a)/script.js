let valor1 = parseInt(prompt("Digite o primeiro valor"))
let valor2 = parseInt(prompt("Digite o segundo valor"))

if (valor1 > valor2) {
    alert("A diferença do maior valor pro menor valor é: " + (valor1 - valor2) )
} else {
    alert("A diferença do maior valor pro menor valor é: " + (valor2 - valor1) ) 
}